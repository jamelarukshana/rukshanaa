import java.util.Scanner;
class PositiveNegative
{
public static voidmain(String args[])
{
int number;
Scanner sc=new Scanner(System.in);
number=sc.nextInt();
System.out.println("Output:"+number);
if(num==0)
  System.out.println("Zero");
if(num<0)
  System.out.println("-ve");
else
  System.out.println("+ve");
  
}
}
